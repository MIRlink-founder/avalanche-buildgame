function quadrantViewBox(paths: { d: string }[], padding = 8): string {
  if (!paths.length) return '0 0 100 50';
  const first = pathBounds(paths[0].d);
  let minX = first.minX;
  let minY = first.minY;
  let maxX = first.maxX;
  let maxY = first.maxY;
  for (let i = 1; i < paths.length; i++) {
    const b = pathBounds(paths[i].d);
    minX = Math.min(minX, b.minX);
    minY = Math.min(minY, b.minY);
    maxX = Math.max(maxX, b.maxX);
    maxY = Math.max(maxY, b.maxY);
  }
  const w = maxX - minX + padding * 2;
  const h = maxY - minY + padding * 2;
  return `${minX - padding} ${minY - padding} ${w} ${h}`;
}

